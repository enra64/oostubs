void Dispatcher::start(Thread& first){
  // globaler interrupts are assumed to be turned off while in this function
  current=&first;
  first.context.set();
}

void Dispatcher::dispatch(Thread& next){
  // globaler interrupts are assumed to be turned off while in this function
  Thread *last = current;
  current = &next;
  last->context.swap(current->context);
}
